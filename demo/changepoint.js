function highlightChangepointsPlugin(ds, cps) {
  return {
    hooks: {
      drawClear: [
        (u) => {
          const { height, top } = u.bbox;
          u.ctx.save();
          let color = "white";
          u.ctx.fillStyle = color;

          function highlightAt(from, to) {
            u.ctx.fillRect(from, top, to - from, height);
          }

          let left = u.valToPos(ds[0], "x", true);
          for (const cp of cps.indices.slice(1)) {
            const right = ds[cp];
            const rightPos = u.valToPos(right, "x", true);
            highlightAt(left, rightPos);
            left = rightPos;
            color = color === "lightgrey" ? "white" : "lightgrey";
            u.ctx.fillStyle = color;
          }
          u.ctx.restore();
        },
      ],
    },
  };
}

function run() {
  let start;

  worker.onmessage = (e) => {
    if (e.data === "ready") {
      start = performance.now();
      worker.postMessage(df);
    } else {
      const changepoints = e.data;
      const elapsed = (performance.now() - start).toFixed(0);
      const uPlotOpts = {
        series: [
          {},
          {
            label: "y",
            stroke: "red",
            width: 1,
          },
        ],
        plugins: [
          highlightChangepointsPlugin(df.ds, changepoints),
          legendAsTooltipPlugin(),
        ],
        ...getSize(),
      };

      const data = [df.ds, df.y];
      const u = new uPlot(
        uPlotOpts,
        data,
        document.getElementById("changepoint-plot"),
      );
      window.addEventListener("resize", () => {
        u.setSize(getSize());
      });
      document.getElementById("changepoint-title").innerText =
        `Changepoint detection with a Bayesian Normal Gamma - done in ${elapsed}ms`;
    }
  };
}

